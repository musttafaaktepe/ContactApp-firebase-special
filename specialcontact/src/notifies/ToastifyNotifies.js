
import {toast} from "react-toastify"


export const NotiInfo = (msg) => {
toast.info((msg), {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "colored",
    });}

